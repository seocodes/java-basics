public class ArrayEX1 {
    public static void main(String[] args) {

        //array = used to store multiple values in a single variable

        String[] cars = {"Camaro","Corvette","Tesla","BMW"};  //the assigned values in the array MUST BE THE SAME DATA TYPE

        cars[0] = "Mustang";   //replaces Camaro by Mustang
        System.out.println(cars[0]);


    }
}