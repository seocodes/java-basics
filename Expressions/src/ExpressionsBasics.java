public class ExpressionsBasics {
    public static void main(String[] args) {

        //expression = operand and operators
        //operands = values, variables, numbers, quantity
        //operators =  + - / % *


        double friends = 10;

        friends++; //incrementation
        friends--; //decrement
        //friends = 10

        friends = (double) friends/3;  //double makes the result a double

        System.out.println(friends);
    }
}